/**
 ******************************************************************************
 * @file    bsp_key.h
 * @brief   按键驱动 — 头文件（纯硬件层，不绑定上层业务逻辑）
 *
 *          设计原则：
 *            - 按键驱动仅负责电平读取 + 消抖，返回"哪个按键被按下"的硬件事件
 *            - 不参与任何业务逻辑（如"按键1=读寄存器"），业务解耦在应用层
 *            - 非阻塞扫描，由主循环以 10ms 间隔调用
 ******************************************************************************
 */

#ifndef __BSP_KEY_H
#define __BSP_KEY_H

#include <stdint.h>

/* ========================= 按键事件枚举 ================================== */

/**
 * @brief 按键触发事件（纯硬件事件，不含业务语义）
 */
typedef enum {
    KEY_EVT_NONE    = 0,  /**< 本周期无按键触发                   */
    KEY_EVT_1_PRESS,      /**< 按键 1 有效按下（消抖通过）         */
    KEY_EVT_2_PRESS,      /**< 按键 2 有效按下（消抖通过）         */
    KEY_EVT_3_PRESS,      /**< 按键 3 有效按下（消抖通过）         */
    KEY_EVT_4_PRESS,      /**< 按键 4 有效按下（消抖通过）         */
} KeyEvent_t;

/* ========================= API 函 数 声 明 =============================== */

/**
 * @brief   非阻塞按键扫描（每 10ms 调用一次）
 * @return  本周期触发的按键事件（单次触发：按住不放仅触发一次）
 *
 * @note    消抖策略：连续 3 次读到低电平（30ms）判定为有效按下，
 *          计数器达 3 后锁死，松手（高电平）后清零解锁。
 */
KeyEvent_t Key_Poll_10ms(void);
uint8_t BSP_Key_ReadStateByte(void);

#endif /* __BSP_KEY_H */
